import mongoose from "mongoose";

// Connect to MongoDB
// mongoose.connect("mongodb://127.0.0.1:27017/proAthlix");

// Create a new schema for the user collection
const userSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
    gender: {
      type: String,
      required: true,
      enum: ["male", "female", "other"],
    },
    followers: [
      {
        type: mongoose.Schema.Types.ObjectId,
        ref: "User",
      },
    ],
    followings: [
      {
        type: mongoose.Schema.Types.ObjectId,
        ref: "User",
      },
    ],
    profilePic: {
      id: String,
      url: String,
    },
  },
  {
    timestamps: true,
  }
);

// Create a new model for the user collection
// module.exports = mongoose.model("User", userSchema);

export const User = mongoose.model("User", userSchema);
